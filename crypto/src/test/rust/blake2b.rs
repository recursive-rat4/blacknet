use blacknet_crypto::random::UniformGenerator;
use blacknet_crypto::symmetric::{Blake2b256, Blake2b512, Blake2xb};
use core::array;

#[test]
fn hash() {
    let message: [u8; 129] = array::from_fn(|i| i as u8);
    let hash256: [u8; 32] = [
        0xF7, 0xF3, 0xC4, 0x6B, 0xA2, 0x56, 0x4F, 0xF4, 0xC4, 0xC1, 0x62, 0xDA, 0x1F, 0x5B, 0x60,
        0x5F, 0x9F, 0x1C, 0x4A, 0xA6, 0xA2, 0x06, 0x52, 0xA9, 0xF9, 0xA3, 0x37, 0xC1, 0xA2, 0xF5,
        0xB9, 0xC9,
    ];
    let hash512: [u8; 64] = [
        0xF5, 0x97, 0x11, 0xD4, 0x4A, 0x03, 0x1D, 0x5F, 0x97, 0xA9, 0x41, 0x3C, 0x06, 0x5D, 0x1E,
        0x61, 0x4C, 0x41, 0x7E, 0xDE, 0x99, 0x85, 0x90, 0x32, 0x5F, 0x49, 0xBA, 0xD2, 0xFD, 0x44,
        0x4D, 0x3E, 0x44, 0x18, 0xBE, 0x19, 0xAE, 0xC4, 0xE1, 0x14, 0x49, 0xAC, 0x1A, 0x57, 0x20,
        0x78, 0x98, 0xBC, 0x57, 0xD7, 0x6A, 0x1B, 0xCF, 0x35, 0x66, 0x29, 0x2C, 0x20, 0xC6, 0x83,
        0xA5, 0xC4, 0x64, 0x8F,
    ];
    assert_eq!(hash256, Blake2b256::digest(message));
    assert_eq!(hash512, Blake2b512::digest(message));
}

#[test]
fn personalization() {
    let personalization: [u8; 16] = array::from_fn(|i| (0x41 + i) as u8);
    let message: [u8; 4] = array::from_fn(|i| (0x61 + i) as u8);
    let hash256: [u8; 32] = [
        0xA0, 0x6C, 0x85, 0xE0, 0x4C, 0x8D, 0x6C, 0x41, 0xEC, 0xDF, 0xDE, 0x5C, 0x3E, 0x10, 0x97,
        0xBE, 0x1A, 0x5C, 0xFB, 0xD9, 0xAB, 0xC0, 0x8B, 0xE3, 0xCD, 0x43, 0xF8, 0x74, 0x40, 0x38,
        0x09, 0x63,
    ];
    let mut hasher = Blake2b256::with_personalization(personalization);
    hasher.update(message);
    assert_eq!(hash256, hasher.finalize());
}

#[test]
fn xof() {
    let message: [u8; 4] = array::from_fn(|i| (0x61 + i) as u8);
    let hash1024: [u8; 128] = [
        0x40, 0xCC, 0x46, 0x18, 0xFA, 0x93, 0xD2, 0x99, 0x14, 0xB2, 0xAE, 0x45, 0x32, 0x7C, 0x9B,
        0xE8, 0x29, 0x80, 0x61, 0x3C, 0x34, 0x20, 0x9F, 0x14, 0x2C, 0xB9, 0x7F, 0xF6, 0x14, 0xE0,
        0x42, 0x5F, 0xE1, 0xD9, 0x75, 0x08, 0xA8, 0x60, 0x4C, 0x63, 0xDB, 0x66, 0x06, 0x30, 0x44,
        0xF5, 0xE4, 0x0D, 0x4C, 0x92, 0xF3, 0xA0, 0xD8, 0xA8, 0x8A, 0xF0, 0xEA, 0xEE, 0x0D, 0x09,
        0x3A, 0x78, 0x42, 0x06, 0x21, 0x4C, 0xEC, 0x6C, 0xF8, 0x5B, 0x01, 0x45, 0x8D, 0x2B, 0x56,
        0xE7, 0x57, 0x90, 0x27, 0x11, 0x53, 0x19, 0xF6, 0xC7, 0x04, 0x01, 0xA2, 0xDC, 0x37, 0x4B,
        0xEB, 0x7B, 0x75, 0x17, 0xDA, 0x4B, 0x8C, 0x15, 0xF4, 0xB5, 0x6C, 0xBF, 0x3D, 0xAE, 0x0A,
        0xA0, 0xE6, 0x9D, 0x65, 0x03, 0xC2, 0x59, 0x48, 0x1C, 0x26, 0xD3, 0x8F, 0xA7, 0x01, 0xEC,
        0x33, 0xF3, 0xD3, 0xE7, 0x36, 0xCF, 0x29, 0x63,
    ];
    let mut hasher = Blake2xb::with_length(hash1024.len() as u32);
    hasher.update(message);
    let mut xof = hasher.finalize();
    let generated: [u8; 128] = array::from_fn(|_| xof.generate());
    assert_eq!(hash1024, generated);
}
