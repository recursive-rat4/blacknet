package ninja.blacknet.crypto

import kotlin.test.Test
import kotlin.test.assertEquals
import ninja.blacknet.util.byteArrayOfInts

class ChaCha20Test {
    private val key = byteArrayOfInts(
        0x81, 0xC7, 0x98, 0xD1, 0xCF, 0xAD, 0xB4, 0x4D, 0x9A, 0xD0, 0xF2, 0xD4, 0x12, 0x15, 0x26, 0xA8,
        0x20, 0x5C, 0xAE, 0x4C, 0x5F, 0x4A, 0xC6, 0x81, 0x7C, 0x93, 0xE6, 0x2D, 0x5C, 0xF9, 0x2D, 0xFC,
    )
    private val iv = byteArrayOfInts(
        0x28, 0x2A, 0x8C, 0xC7, 0x44, 0x99, 0x12, 0xD8, 0x39, 0xF8, 0x6B, 0x3D,
    )
    private val decrypted = byteArrayOfInts(
        0xF7, 0xB7, 0xFE, 0xEC, 0xB8, 0x49, 0xA2, 0x7D, 0x6A, 0xBA, 0xB0, 0xD4, 0x50, 0x54, 0xA6, 0xCA,
        0x88, 0xCA, 0xDA, 0x11, 0xFF, 0x3B, 0xBF, 0xE6, 0x68, 0x69, 0x24, 0x8E, 0xF5, 0x56, 0xE5, 0xC3,
    )
    private val encrypted = byteArrayOfInts(
        0xEF, 0x9D, 0xD7, 0xB6, 0xDC, 0x19, 0x76, 0x80, 0x77, 0xE7, 0xAC, 0x4E, 0xD5, 0x3C, 0x04, 0x6D,
        0xA2, 0x7C, 0x64, 0xC5, 0x59, 0xC4, 0xAB, 0x9A, 0x0A, 0xA9, 0xDE, 0xC9, 0x75, 0xA5, 0x0F, 0x66,
    )

    @Test
    fun decrypt() {
        val box = iv + encrypted
        assertEquals(decrypted, ChaCha20.decrypt(key, box))
    }

    @Test
    fun encrypt() {
        val box = iv + encrypted
        assertEquals(box, ChaCha20.encrypt(key, iv, decrypted))
    }
}
